package java_bean;

public class clothe_bean {
	int ClotheId;
	String ClotheName;
	double ClothePrice;
	String Clothetype;
	String ClotheImg;
	String ClothefactoryAddress;
	public int getClotheId() {
		return ClotheId;
	}
	public void setClotheId(int string) {
		ClotheId = string;
	}
	public String getClotheName() {
		return ClotheName;
	}
	public void setClotheName(String clotheName) {
		ClotheName = clotheName;
	}
	public double getClothePrice() {
		return ClothePrice;
	}
	public void setClothePrice(double clothePrice) {
		ClothePrice = clothePrice;
	}
	public String getClothetype() {
		return Clothetype;
	}
	public void setClothetype(String clothetype) {
		Clothetype = clothetype;
	}
	public String getClotheImg() {
		return ClotheImg;
	}
	public void setClotheImg(String clotheImg) {
		ClotheImg = clotheImg;
	}
	public String getClothefactoryAddress() {
		return ClothefactoryAddress;
	}
	public void setClothefactoryAddress(String clothefactoryAddress) {
		ClothefactoryAddress = clothefactoryAddress;
	}
}
